                    <p><strong>Imported States</strong>: Punjab, Uttar Pradesh, Maharashtra.</p>
                    <p><strong>Reason for Importing</strong>: India’s agricultural sector requires a large amount of fertilizers to increase crop yields. Due to limited local production of certain fertilizers, imports are necessary to meet the needs of farmers.</p>
                </div>
            </div>
            <div class="card">
                <img src="../public/images/imports/section2/card7.jpg" alt="Iron and Steel">
                <div class="card-content">
