                <div class="card-content">
                    <h4>1. Petroleum Products</h4>
                    <p><strong>Origin Countries</strong>: Iraq, Saudi Arabia, UAE, Russia, and the USA.</p>
                    <p><strong>Imported States</strong>: Maharashtra, Gujarat, Tamil Nadu.</p>
                    <p><strong>Reason for Importing</strong>: India relies heavily on crude oil to meet its energy needs, as domestic production is insufficient. Petroleum products are also critical for transportation, industries, and power generation.</p>
                </div>
            </div>
            <div class="card">
                <img src="../public/images/imports/section2/card2.jpg" alt="Electronic Goods">
                <div class="card-content">
                    <h4>2. Electronic Goods</h4>
                    <p><strong>Origin Countries</strong>: China, South Korea, Vietnam, Taiwan.</p>
                    <p><strong>Imported States</strong>: Karnataka, Maharashtra, Tamil Nadu.</p>
                    <p><strong>Reason for Importing</strong>: The growing demand for electronics, such as smartphones, laptops, semiconductors, and consumer gadgets, outpaces domestic manufacturing capabilities. India’s electronics market is one of the fastest-growing sectors, leading to increased imports.</p>
                </div>
            </div>
            <div class="card">
                <img src="../public/images/imports/section2/card3.jpg" alt="Precious Metals">
