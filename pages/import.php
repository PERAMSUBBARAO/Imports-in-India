                    <p><strong>Origin Countries</strong>: Switzerland, UAE, South Africa.</p>
                    <p><strong>Imported States</strong>: Maharashtra, Kerala, Gujarat.</p>
                    <p><strong>Reason for Importing</strong>: India has a high demand for gold and silver, driven by cultural factors such as weddings, festivals, and investments. Despite having some domestic reserves, these precious metals are not enough to meet the demand, thus prompting significant imports.</p>
                </div>
            </div>
            <div class="card">
                <img src="../public/images/imports/section2/card4.jpg" alt="Machinery and Equipment">
                <div class="card-content">
                    <h4>4. Machinery and Equipment</h4>
                    <p><strong>Origin Countries</strong>: Germany, China, Japan, South Korea.</p>
                    <p><strong>Imported States</strong>: Maharashtra, Gujarat, Tamil Nadu.</p>
                    <p><strong>Reason for Importing</strong>: To support industrial growth, infrastructure development, and technological advancements, India imports a variety of machinery and equipment. This includes machines for manufacturing, construction, and the energy sector.</p>
                </div>
            </div>
            <div class="card">
                <img src="../public/images/imports/section2/card5.jpg" alt="Organic Chemicals">
                <div class="card-content">
                    <h4>5. Organic Chemicals</h4>
                    <p><strong>Origin Countries</strong>: China, Saudi Arabia, Germany.</p>
                    <p><strong>Imported States</strong>: Gujarat, Maharashtra.</p>
                    <p><strong>Reason for Importing</strong>: Organic chemicals are used in industries like pharmaceuticals, textiles, and plastics. India lacks sufficient domestic production capacity to meet the growing demand for these chemicals.</p>
                </div>
            </div>
            <div class="card">
